using System;
using System.Collections.Generic;
using System.Text;

namespace ConsoleApp1
{
    class SquaresDisplay1TO50ForLoop
    {
        static void Main(string[] args)
        {
            Console.WriteLine("The Squares Of 1 to 50 IS:");
            for(int i=1;i<=50;i++)
            {
                Console.WriteLine(i*i);
            }
        }
    }
}
